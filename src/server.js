const express = require('express');
const cors = require('cors');
require('dotenv').config();

// Import routes à¹à¸¥à¸° middleware
const memberRoutes = require('../src/routes/memberRoutes');
const { requestLogger, errorHandler, notFound, validateJSON } = require('../src/middleware');

const app = express();
const PORT = process.env.PORT || 3001;

// Middleware
app.use(cors());
app.use(express.json({ limit: '10mb' }));
app.use(express.urlencoded({ extended: true }));
app.use(validateJSON);
app.use(requestLogger);

// Health check endpoint
app.get('/health', (req, res) => {
  res.status(200).json({
    success: true,
    message: 'Server is running',
    timestamp: new Date().toISOString(),
    version: process.env.npm_package_version || '1.0.0'
  });
});

// API Routes
app.use('/api/members', memberRoutes);

// Handle 404
app.use(notFound);

// Error handling middleware (à¸•à¹‰à¸­à¸‡à¸­à¸¢à¸¹à¹ˆà¸—à¹‰à¸²à¸¢à¸ªà¸¸à¸”)
app.use(errorHandler);

// Start server
app.listen(PORT, () => {
  console.log(`ðŸš€ Server is running on port ${PORT}`);
  console.log(`ðŸ“Š Health check: http://localhost:${PORT}/health`);
  console.log(`ðŸ‘¥ Members API: http://localhost:${PORT}/api/members`);
});

// Handle graceful shutdown
process.on('SIGTERM', () => {
  console.log('SIGTERM received. Shutting down gracefully...');
  process.exit(0);
});

process.on('SIGINT', () => {
  console.log('SIGINT received. Shutting down gracefully...');
  process.exit(0);
});